# min() and max() functions can be used to 
# find the lowest or highest value in an iterable.

x = min(10,5,6)
print('Min value is: ', x)
#Min value is:  5

y = max(10,5,16, 8)
print('Max value is: ', y)
# Max value is:  16

#abs() function returns the absolute- positive value of specified number.

z = abs(-1.234)
print('Absolute value is: ', z)
#Absolute value is:  1.234

#pow(x,y) function returns the value of x to the power y
a = pow(2,4)
print('Power: ',a)
#Power:  16

import math

s= math.sqrt(625)
print('Square Root: ',s)
#Square Root:  25.0

t= math.ceil(1.3)
print('Ceil : ',t)
#Ceil :  2

u = math.floor(1.3)
print('Floor : ',u)
# Floor :  1

pi = math.pi
print('pi : ',pi)
#pi :  3.14

